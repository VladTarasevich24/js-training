function add(a, b) {
    return a + b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function mod(a, b) {
    return a % b;
}

function exponent(a, b) {
    return Math.pow(a, b);
}

function subt(a, b) {
    return a - b;
}


console.log(add(5, 3));
console.log(multiply(5, 3));
console.log(divide(10, 2));
console.log(mod(10, 3));
console.log(exponent(2, 3));
console.log(subt(10, 3));